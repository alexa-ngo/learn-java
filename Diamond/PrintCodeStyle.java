public class PrintCodeStyle {
	public static void main(String[] args)
	{
		System.out.println("I am a line...");
		System.out.println("[MARKER]");
		
		System.out.print("I am not a line..");
		System.out.print("[MARKER]");
		
		System.out.print("I am a line again...\n");
		System.out.println("[MARKER]");
		
		System.out.print("RILEY\n");
		System.out.println();
		System.out.print();
	}
}